/**
 * DTO: UpdatePostDto (게시글 수정 데이터 전송 객체)
 *
 * 게시글 수정 시 사용하는 DTO입니다.
 * - 모든 필드가 선택적입니다 (부분 업데이트 지원)
 * - boardId는 수정할 수 없습니다 (게시글을 다른 게시판으로 이동하는 것은 별도 기능)
 *
 * boardId가 없는 이유:
 * - 게시글의 소속 게시판을 변경하는 것은 일반적으로 별도의 기능입니다
 * - 실수로 게시판을 변경하는 것을 방지합니다
 * - 필요하다면 별도의 DTO나 엔드포인트를 만들 수 있습니다
 */
export class UpdatePostDto {
  /**
   * 게시글 제목
   * - 선택적 필드
   */
  title?: string;

  /**
   * 게시글 내용
   * - 선택적 필드
   */
  content?: string;
}
